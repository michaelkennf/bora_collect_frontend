// Fonction utilitaire pour obtenir les quartiers d'une commune
export const getQuartiersByCommune = (province: string, city: string, commune: string) => {
  const provinceData = quartiersData[province as keyof typeof quartiersData];
  if (!provinceData) return [];
  
  const cityData = provinceData.cities.find(c => c.name === city);
  if (!cityData) return [];
  
  const communeData = cityData.communes.find(c => c.name === commune);
  return communeData?.quartiers || [];
};
