// Fonction utilitaire pour obtenir les villes d'une province
export const getCitiesByProvince = (province: string) => {
  return citiesData[province as keyof typeof citiesData]?.cities || [];
};

// Fonction utilitaire pour obtenir les communes d'une ville dans une province
export const getCommunesByCity = (province: string, city: string) => {
  const provinceData = citiesData[province as keyof typeof citiesData];
  if (!provinceData) return [];
  
  const cityData = provinceData.cities.find(c => c.name === city);
  return cityData?.communes || [];
};
